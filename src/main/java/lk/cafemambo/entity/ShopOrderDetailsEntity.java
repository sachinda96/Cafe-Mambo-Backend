package lk.cafemambo.entity;

import javax.persistence.*;
import java.util.Date;

@Entity(name = "SHOPORDERDETAILS")
public class ShopOrderDetailsEntity {

    @Id
    private String id;

    @Column(length = 10)
    private String status;

    @Column(length = 50)
    private String createBy;

    private Date createDate;

    @Column(length = 50)
    private String updateBy;

    private Date updateDate;

    private Double price;

    private Integer qty;

    @ManyToOne
    @JoinColumn(name = "shoporderid")
    private ShopOrderEntity shopOrderEntity;

    @ManyToOne
    @JoinColumn(name = "itemid")
    private ItemEntity itemEntity;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getCreateBy() {
        return createBy;
    }

    public void setCreateBy(String createBy) {
        this.createBy = createBy;
    }

    public Date getCreateDate() {
        return createDate;
    }

    public void setCreateDate(Date createDate) {
        this.createDate = createDate;
    }

    public String getUpdateBy() {
        return updateBy;
    }

    public void setUpdateBy(String updateBy) {
        this.updateBy = updateBy;
    }

    public Date getUpdateDate() {
        return updateDate;
    }

    public void setUpdateDate(Date updateDate) {
        this.updateDate = updateDate;
    }

    public Double getPrice() {
        return price;
    }

    public void setPrice(Double price) {
        this.price = price;
    }

    public Integer getQty() {
        return qty;
    }

    public void setQty(Integer qty) {
        this.qty = qty;
    }

    public ShopOrderEntity getShopOrderEntity() {
        return shopOrderEntity;
    }

    public void setShopOrderEntity(ShopOrderEntity shopOrderEntity) {
        this.shopOrderEntity = shopOrderEntity;
    }

    public ItemEntity getItemEntity() {
        return itemEntity;
    }

    public void setItemEntity(ItemEntity itemEntity) {
        this.itemEntity = itemEntity;
    }
}
